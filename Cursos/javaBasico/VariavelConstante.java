public class VariavelConstante{
	
	public static void main(String[] args){
			
		//Variável Constante 
		final double PI =  3.141592653589793; //não pode ser alterado o valor contido dentro desta variavel
											  //Códgo não permite ser copilado, pois esse valor é Constante 
		System.out.print(PI);
	
	}
}