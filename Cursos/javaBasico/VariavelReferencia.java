public class VariavelReferencia{

	public static void main(String[] args){
		
		//Variável Primitiva		
		int x = 7; //recebe valor ao ser declarada
		x = 9; //recebe novo valor '9', apaga o antigo '7'
		
		
		//Variável de Referência
		String y = "XTI"; //recebe e referencia o valor declarado
		y = "wwww.xti.com";//recebe novo valor referenciado e mantem em memória o antigo onde o Gal
		
		System.out.print(x);
	
	}
}